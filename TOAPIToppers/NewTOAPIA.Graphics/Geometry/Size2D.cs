namespace NewTOAPIA.Graphics
{
    public struct Size2D
    {
        public double width;
        public double height;

        public Size2D(double width_, double height_)
        {
            width = width_;
            height = height_;
        }
    }

    public struct Size2F
    {
        public double width;
        public double height;

        public Size2F(double width_, double height_)
        {
            width = width_;
            height = height_;
        }
    }
}