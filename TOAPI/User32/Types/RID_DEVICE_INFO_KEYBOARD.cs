using System.Runtime.InteropServices;

namespace TOAPI.User32
{
    [StructLayout(LayoutKind.Sequential)]
    public struct RID_DEVICE_INFO_KEYBOARD
    {
        public uint dwType;                     // Type of the keyboard.
        public uint dwSubType;                  // Subtype of the keyboard.
        public uint dwKeyboardMode;
        public uint dwNumberOfFunctionKeys;     // Number of function keys
        public uint dwNumberOfIndicators;       // Number of LED Indicators on the keyboard
        public uint dwNumberOfKeysTotal;        // Total number of keys (function, keypad, regular, extras)
    }
}